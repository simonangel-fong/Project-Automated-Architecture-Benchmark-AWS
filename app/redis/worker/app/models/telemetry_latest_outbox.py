# app/models/telemetry_latest_outbox.py
from __future__ import annotations

from datetime import datetime
from uuid import UUID as UUID_Type
from typing import Any, Dict, Optional

from sqlalchemy import BigInteger, DateTime, Integer, Text
from sqlalchemy.dialects.postgresql import UUID as PG_UUID, JSONB
from sqlalchemy.orm import Mapped, mapped_column
from sqlalchemy.sql import func

from .base import Base


class TelemetryLatestOutbox(Base):
    """
    Outbox messages produced from telemetry_event inserts.
    """

    __tablename__ = "telemetry_latest_outbox"
    __table_args__ = {"schema": "app"}

    # surrogate key
    outbox_id: Mapped[int] = mapped_column(
        BigInteger,
        primary_key=True,
        doc="Surrogate key generated by the database for this outbox row.",
    )

    # business key (idempotency anchor)
    telemetry_event_id: Mapped[int] = mapped_column(
        BigInteger,
        nullable=False,
        doc="Unique reference to the source telemetry_event.id (idempotency key).",
    )

    # partition key for Redis
    device_uuid: Mapped[UUID_Type] = mapped_column(
        PG_UUID(as_uuid=True),
        nullable=False,
        doc="Device UUID used for routing (e.g., Redis key partition).",
    )

    # metadata
    event_type: Mapped[str] = mapped_column(
        Text,
        nullable=False,
        server_default="TELEMETRY_EVENT_INSERTED",
        doc="Type of outbox message.",
    )

    system_time_utc: Mapped[datetime] = mapped_column(
        DateTime(timezone=True),
        nullable=False,
        doc="Server-side UTC timestamp copied from telemetry_event.",
    )

    created_at: Mapped[datetime] = mapped_column(
        DateTime(timezone=True),
        nullable=False,
        server_default=func.now(),
        doc="Timestamp when the outbox row was created.",
    )

    # delivery management
    status: Mapped[str] = mapped_column(
        Text,
        nullable=False,
        server_default="NEW",
        doc="Delivery status: NEW, PROCESSING, PROCESSED, FAILED.",
    )

    attempts: Mapped[int] = mapped_column(
        Integer,
        nullable=False,
        server_default="0",
        doc="Number of processing attempts by the worker.",
    )

    processed_at: Mapped[Optional[datetime]] = mapped_column(
        DateTime(timezone=True),
        nullable=True,
        doc="Timestamp when the outbox row was successfully processed.",
    )

    last_error: Mapped[Optional[str]] = mapped_column(
        Text,
        nullable=True,
        doc="Last error message if processing failed.",
    )

    # message payload
    payload: Mapped[Dict[str, Any]] = mapped_column(
        JSONB,
        nullable=False,
        doc="JSON payload used by downstream consumers (e.g., Redis sync).",
    )

    def __repr__(self) -> str:
        return (
            f"<TelemetryLatestOutbox outbox_id={self.outbox_id} "
            f"telemetry_event_id={self.telemetry_event_id} device_uuid={self.device_uuid} "
            f"status={self.status} attempts={self.attempts} "
            f"system_time_utc={self.system_time_utc} created_at={self.created_at}>"
        )
